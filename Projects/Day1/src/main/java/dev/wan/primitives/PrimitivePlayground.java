package dev.wan.primitives;

public class PrimitivePlayground {
    public static void main (String [] args) {
        // Java has 8 primitives
        // primitives are not objects

        boolean b = true;
        byte by = 0;        // 2 byte
        short s = 0;        // 2 byte
        char c = 'c';       // 2 byte
        int i = 100;        // 4 byte
        float f = 100;      // 4 byte
        long l = 10000;     // 8 byte
        double d = 100000;  // 8 byte

        // STRING IS NOT A PRIMITIVE
        // decimals default eval to double. To not do that, type f,l,d at then end

    }
}
